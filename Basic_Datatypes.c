/// WAP to check Basic Datatypes in C.
#include <stdio.h>
int main() {
    int a = 10;
    char b = 'X';
    float c = 3.14526;
    double d = 3.14159265358979323846;
    printf("integer: %d\n", a);
    printf("character: %c\n", b);
    printf("float: %f\n", c);
    printf("double: %lf\n", d);
    return 0;
}